List = [5, 7, 2, 9, 4, 1, 3]

print("\nQuantidade de elementos da lista: ", len(List))
print("\nMaior elemento da lista: ", max(List))
print("\nMenor elemento da lista: ", min(List))
print("\nSoma dos elementos da lista: ", sum(List))
print("\nA lista em ordem crescente: ", sorted(List))
print("\nA lista em ordem decrescente: ", sorted(List, reverse=True))

